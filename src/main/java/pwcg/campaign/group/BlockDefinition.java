package pwcg.campaign.group;

public class BlockDefinition
{
    private String name;
    private String desc;
    private BlockType type;
    private int durability;

    public String getName()
    {
        return name;
    }

    public String getDesc()
    {
        return desc;
    }

    public BlockType getType()
    {
        return type;
    }

    public int getDurability()
    {
        return durability;
    }
}
