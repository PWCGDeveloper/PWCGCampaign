package pwcg.campaign.io.transport;

public class MapTransportSystem
{
    private double mapWidth = 0;
    private double mapHeight = 0;
    private double mapScaleFactor = 0;

    public double getMapWidth()
    {
        return mapWidth;
    }

    public void setMapWidth(double mapWidth)
    {
        this.mapWidth = mapWidth;
    }

    public double getMapHeight()
    {
        return mapHeight;
    }

    public void setMapHeight(double mapHeight)
    {
        this.mapHeight = mapHeight;
    }

    public double getMapScaleFactor()
    {
        return mapScaleFactor;
    }

    public void setMapScaleFactor(double mapScaleFactor)
    {
        this.mapScaleFactor = mapScaleFactor;
    }

}
